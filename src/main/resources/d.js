  function checkBussniessLeader(){
    var deptId=$("input[name='oct_count_sign.business_depart_id']").val();
    if(deptId){
                $.ajax({
                		url: "departController.do?getdepartLeaderList",
                		dataType: 'json',
                		type: 'post',
                		data: {
                            "departids": deptId
                        },
                        async: false,
                		success: function(data) {
                                    if(data.obj.length > 0){
                                        var Leader = data.obj[0].leader_id || data.obj[0].leader || data.obj[0].manager_id || data.obj[0].manager || data.obj[0].dep_leader || data.obj[0].dep_leader_id; //部门负责人
                                        var DLeader = data.obj[0].dep_leader || data.obj[0].dep_leader_id;   //分管经理
                                        var MLeader = data.obj[0].manager_id || data.obj[0].manager||data.obj[0].subLeader || data.obj[0].subCenterLeader|| data.obj[0].dep_leader || data.obj[0].dep_leader_id;   //中心负责人
                                        var SLeader = data.obj[0].subLeader || data.obj[0].subCenterLeader|| data.obj[0].dep_leader || data.obj[0].dep_leader_id; //分子公司负责人
                                        var leader=Leader.split(";");
                                        var manager = MLeader.split(";");
                                        var depLeader = DLeader.split(";");
                                        var subLeader = SLeader.split(";");
                                        //部门领导
                                        $("#oct_count_sign_business_depart_leader").val(leader[2]);
                                        //中心领导business_manager
                                        if(manager.length > 2){
                                            $('input[name="oct_count_sign.business_manager"]').val(manager[2]);
                                        }
                                        //分管领导
                                        if(depLeader.length > 2){
                                            $('input[name="oct_count_sign.business_depleader"]').val(depLeader[2]);
                                        }
                                        //分子公司负责人
                                        if(SLeader != ''){
                                            $('input[name="oct_count_sign.business_subleader"]').val(subLeader[2]);
                                        }
                                    }
                                },
                                error:function(data){
                                    console.log(data);
                                }
                	});
            }
  }
